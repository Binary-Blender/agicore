function emitProviderFromModel(providers: string[]): string {
  const lines: string[] = [];
  lines.push('fn provider_from_model(model: &str) -> &\'static str {');
  // Order matters: longer/more-specific prefixes first. We iterate an explicit
  // priority list rather than relying on enumeration order.
  const prefixMap: Array<[string[], string]> = [];
  if (providers.includes('anthropic')) prefixMap.push([['claude'], 'anthropic']);
  if (providers.includes('openai')) prefixMap.push([['gpt-', 'o1', 'o3', 'o4', 'chatgpt-'], 'openai']);
  if (providers.includes('google')) prefixMap.push([['gemini'], 'google']);
  if (providers.includes('xai')) prefixMap.push([['grok'], 'xai']);
  if (providers.includes('huggingface')) prefixMap.push([['hf_', 'huggingface/'], 'huggingface']);

  for (const [prefixes, provider] of prefixMap) {
    const cond = prefixes.map(p => `model.starts_with("${p}")`).join(' || ');
    lines.push(`    if ${cond} { return "${provider}"; }`);
  }
  // Default fallback
  const fallback = providers[0] || 'anthropic';
  lines.push(`    "${fallback}"`);
  lines.push('}');
  return lines.join('\n');
}

function emitSendChat(providers: string[], skipped: string[]): string {
  const lines: string[] = [];
  lines.push('#[tauri::command]');
  lines.push('pub async fn send_chat(');
  lines.push('    request: ChatRequest,');
  lines.push('    request_id: String,');
  lines.push('    window: Window,');
  lines.push('    store: tauri::State<\'_, ApiKeyStore>,');
  lines.push(') -> Result<ChatResponse, String> {');
  lines.push('    let provider = provider_from_model(&request.model);');
  lines.push('    let api_key = {');
  lines.push('        let keys = store.lock().map_err(|e| e.to_string())?;');
  lines.push('        keys.get(provider).cloned()');
  lines.push('    };');
  lines.push('    let api_key = api_key.ok_or_else(|| format!("No API key configured for {}", provider))?;');
  lines.push('');
  // Document any AI_SERVICE.PROVIDERS that we are skipping here — they have
  // no chat dispatch template but we still want their API keys stored so the
  // ROUTER tier can use them. Anyone reading the generated send_chat shouldn't
  // be surprised by the missing match arm.
  for (const p of skipped) {
    lines.push(`    // Note: \`${p}\` is declared in AI_SERVICE.PROVIDERS but has no chat`);
    lines.push(`    // dispatch template — it is treated as key-storage-only. Routing to`);
    lines.push(`    // the ${p} endpoint happens through the ROUTER tier, not send_chat.`);
  }
  lines.push('    match provider {');
  for (const p of providers) {
    lines.push(`        "${p}" => call_${p}(request, request_id, window, api_key).await,`);
  }
  lines.push('        _ => Err(format!("Unsupported provider: {}", provider)),');
  lines.push('    }');
  lines.push('}');
  return lines.join('\n');
}

