// AI Service Code Generator
// Emits a multi-provider Rust ai_service.rs (chat + streaming) plus a
// model-discovery service from the AI_SERVICE block of an .agi file.
//
// Provider templates encode each vendor's wire protocol. Each provider gets
// a `call_<provider>` async fn invoked by `send_chat`. Reasoning models
// (o1/o3/o4/gpt-5*) get `max_completion_tokens` instead of `max_tokens` and
// no temperature. Google uses true SSE via `:streamGenerateContent?alt=sse`.
// Streams that die mid-response return whatever partial content arrived
// instead of erroring out (matching the 3G TS impl).

