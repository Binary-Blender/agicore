const ANTHROPIC_TEMPLATE = `async fn call_anthropic(
    request: ChatRequest,
    request_id: String,
    window: Window,
    api_key: String,
) -> Result<ChatResponse, String> {
    let client = reqwest::Client::new();

    // Anthropic separates the system prompt from the message list
    let messages: Vec<serde_json::Value> = request.messages.iter()
        .filter(|m| m.role != "system")
        .map(|m| serde_json::json!({"role": m.role, "content": m.content}))
        .collect();

    let max_tokens = request.max_tokens.unwrap_or(8192);
    let mut body = serde_json::json!({
        "model": request.model,
        "max_tokens": max_tokens,
        "temperature": request.temperature.unwrap_or(0.7),
        "messages": messages,
        "stream": true,
    });
    if let Some(sys) = &request.system_prompt {
        body["system"] = serde_json::json!(sys);
    }

    let response = client
        .post("https://api.anthropic.com/v1/messages")
        .header("x-api-key", &api_key)
        .header("anthropic-version", "2023-06-01")
        .header("content-type", "application/json")
        .json(&body)
        .send()
        .await
        .map_err(|e| e.to_string())?;

    if !response.status().is_success() {
        let err_text = response.text().await.unwrap_or_default();
        return Err(format!("Anthropic API error: {}", err_text));
    }

    let mut full_content = String::new();
    let mut input_tokens = 0u32;
    let mut output_tokens = 0u32;
    let mut bytes_stream = response.bytes_stream();
    use futures_util::StreamExt;

    let stream_result: Result<(), String> = async {
        while let Some(chunk) = bytes_stream.next().await {
            let chunk = chunk.map_err(|e| e.to_string())?;
            let text = String::from_utf8_lossy(&chunk);
            for line in text.lines() {
                if let Some(data) = line.strip_prefix("data: ") {
                    if data == "[DONE]" { continue; }
                    if let Ok(event) = serde_json::from_str::<serde_json::Value>(data) {
                        let event_type = event["type"].as_str().unwrap_or("");
                        if event_type == "content_block_delta" {
                            if let Some(delta_text) = event["delta"]["text"].as_str() {
                                full_content.push_str(delta_text);
                                let _ = window.emit("chat-stream", StreamDelta {
                                    request_id: request_id.clone(),
                                    delta: delta_text.to_string(),
                                    done: false,
                                });
                            }
                        } else if event_type == "message_start" {
                            if let Some(t) = event["message"]["usage"]["input_tokens"].as_u64() {
                                input_tokens = t as u32;
                            }
                        } else if event_type == "message_delta" {
                            if let Some(t) = event["usage"]["output_tokens"].as_u64() {
                                output_tokens = t as u32;
                            }
                        }
                    }
                }
            }
        }
        Ok(())
    }.await;

    // Partial-content recovery: if the stream died but we got something,
    // return what we have rather than failing the whole request.
    if let Err(err) = stream_result {
        if full_content.is_empty() {
            return Err(format!("Anthropic stream failed: {}", err));
        }
        eprintln!("[Anthropic] stream interrupted after {} chars: {}", full_content.len(), err);
    }

    let _ = window.emit("chat-stream", StreamDelta {
        request_id: request_id.clone(),
        delta: String::new(),
        done: true,
    });

    Ok(ChatResponse {
        content: full_content,
        model: request.model,
        provider: "anthropic".to_string(),
        input_tokens,
        output_tokens,
    })
}`;

