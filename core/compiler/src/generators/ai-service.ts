/**
 * Translate a KEYS_FILE / VAULT path that may contain `%APPDATA%` placeholders
 * into a Rust expression that resolves to a PathBuf at runtime. Falls back to
 * `$HOME/.config/<AppDir>` on non-Windows hosts.
 */
function emitKeysPathFn(keysFile: string): string {
  // Split off the %APPDATA% prefix if present
  const hasAppdata = keysFile.startsWith('%APPDATA%');
  let suffix = hasAppdata ? keysFile.slice('%APPDATA%'.length) : keysFile;
  // Normalize leading slashes
  suffix = suffix.replace(/^[\\/]+/, '');
  // Split on / or \ — emit as PathBuf::join calls
  const parts = suffix.split(/[\\/]+/).filter(Boolean);

  const lines: string[] = [];
  lines.push('fn api_keys_path() -> PathBuf {');
  if (hasAppdata) {
    lines.push('    let mut base = std::env::var("APPDATA")');
    lines.push('        .ok()');
    lines.push('        .map(PathBuf::from)');
    lines.push('        .or_else(|| std::env::var("HOME").ok().map(|h| PathBuf::from(h).join(".config")))');
    lines.push('        .unwrap_or_else(|| PathBuf::from("."));');
  } else {
    const first = suffix.split(/[\\/]+/)[0] || '.';
    lines.push(`    let mut base = PathBuf::from(${JSON.stringify(first)});`);
    parts.shift();
  }
  for (const part of parts) {
    lines.push(`    base = base.join(${JSON.stringify(part)});`);
  }
  lines.push('    base');
  lines.push('}');
  return lines.join('\n');
}

