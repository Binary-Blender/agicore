// ─── Utility: parse window string to days ─────────────────────────────────────

function parseWindowToDays(window: string): number {
  const match = window.match(/^(\d+)([hdwm])$/);
  if (!match) return 7;
  const n = parseInt(match[1]!);
  switch (match[2]) {
    case 'h': return Math.ceil(n / 24);
    case 'd': return n;
    case 'w': return n * 7;
    case 'm': return n * 30;
    default:  return 7;
  }
}

// ─── Utility: build input query from REASONER input spec ─────────────────────

function buildInputQuery(r: ReasonerDecl, ast: AgiFile): string {
  const channels = r.input.channels;
  const window = r.input.window ?? '7d';
  const filter = r.input.filter;

  // Map channel name → entity table (best-effort: channel name ≈ snake_case entity name)
  const parts = channels.map((channel) => {
    const table = toSnake(channel);
    const whereClause = `created_at > datetime('now', '-{window} days')${filter ? ` AND ${filter}` : ''}`;
    return `SELECT * FROM ${table} WHERE ${whereClause} ORDER BY created_at DESC LIMIT 200`;
  });

  if (parts.length === 1) return parts[0]!;
  return parts.join(' UNION ALL ') + ' LIMIT 300';
}
