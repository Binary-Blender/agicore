import type { AgiFile, ReasonerDecl } from '@agicore/parser';

// ─── Main entry point ─────────────────────────────────────────────────────────

export function generateReasoner(ast: AgiFile): Map<string, string> {
  const files = new Map<string, string>();
  if (ast.reasoners.length === 0) return files;

  const rs = generateReasonerRs(ast);
  if (rs) files.set('src-tauri/src/commands/reasoner.rs', rs);

  files.set('migrations/reasoner_runs.sql', generateReasonerMigration());

  const tsx = generateReasonerViewTsx(ast);
  if (tsx) files.set('src/components/ReasonerView.tsx', tsx);

  return files;
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function toSnake(s: string): string {
  return s.replace(/[^a-zA-Z0-9]+/g, '_').replace(/([A-Z])/g, '_$1').toLowerCase().replace(/^_/, '');
}

function scheduleToThresholdHours(schedule: string): number | null {
  switch (schedule) {
    case 'hourly':  return 1;
    case 'daily':   return 22;
    case 'weekly':  return 160;
    default:        return null; // on_demand / event_triggered / cron — no auto-run
  }
}

